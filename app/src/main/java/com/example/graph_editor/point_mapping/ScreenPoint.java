package com.example.graph_editor.point_mapping;

public class ScreenPoint {
    private final float x;
    private final float y;

    public ScreenPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }
    public float getY() {
        return y;
    }
}
